<h1>Get started</h1>

MxDev ontwikkelt high-end websites in de front-end en back-end van hoge kwaliteit.
Ook geven wij het beste advies welk soort website het beste bij uw branche past.

Na ontvangst van een reactie op de offerte gaan wij gaan het gesprek met de klant.
Tijdens het gesprek, vragen wij aan de klant wat voor soort website het moet worden en wat zijn eventuele wensen zijn voordat we gaan beginnen met het ontwikkelen.
Na het ontvangen van de beantwoorde vragen, geven wij de klant verschillende keuzes voor zijn/haar website en geven ook daarbij aan welk soort website het beste bij zijn/haar branche past.
Na een akkoordbevestiging van de klant, gaan wij aan de slag met de planning en SCRUM.
Tenslotte gaan wij dan aan de website beginnen met ieder een eigen taak (Front-End, Back-End, Fullstack etc.).
